     * 创建目录
     * @parame $dir_name 目录名
     * @return boolean true 成功, false 失败
     */
    static public function mk_dir($dir_name){
        $dir_name = rtrim($dir_name, '/').'/';
        if(!is_dir($dir_name)){
            if(mkdir($dir_name, 0700)==false){
                return false;
            }
            return true;
        }
        return true;
    }

